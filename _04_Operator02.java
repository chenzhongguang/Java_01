public class _04_Operator02 {
  public static void main(String[] args) {
    int a = 10;
    int b = 20;
    a +=10;
    System.out.println("a:"+a);

    System.out.println(a + b);
    System.out.println(a - b);
    System.out.println(a * b);
    System.out.println(a / b);
    System.out.println(a % b);
  }
}
